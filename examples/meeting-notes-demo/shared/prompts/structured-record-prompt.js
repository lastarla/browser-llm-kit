import template from '../config/template.js';

export default function buildStructuredRecordPrompt({
  currentDateTime,
  fullName,
  position,
  corpus,
}) {
  return `你要把客户沟通语料整理成结构化记录。

要求：
1. 只依据语料填写，不要编造。
2. 如果语料未提及，就写“未提及”。
3. 不要重复字段名作为字段值。例如“沟通主题”下面不能再写“沟通主题”。
4. 不要输出说明文字、不要输出示例、不要输出代码块围栏。
5. 不要输出“已完成”“输出要求”“最终输出格式”等提示语。
6. 第一行必须直接是“### 1. 基础信息”。
7. 输出语言使用中文。
8. 保留模板原有标题层级，不要新增标题。
9. 沟通时间优先提取语料中的相对时间或模糊时间，例如“下午”“今天上午”“下周三”；只有完全没提到任何时间信息时才写“未提及”。不要把当前时间 ${currentDateTime} 当成沟通时间。
10. 参会人员必须同时提取我方和客户方。语料里未直接给出我方姓名时，优先写“我方：我”，不要写“未提供”“未提供（我方）”。
11. “对现状的不满或希望达成的目标”要尽量保留语料中的具体场景，例如混合式教学、在线作业、课后回看、在线小组讨论，不要只写抽象总结。
12. “客户背景与现状”中尽量保留背景事实，例如扩招、专业增多、教学管理方式传统。
13. “任务内容及负责人、计划完成时间”下的每一项行动项，都必须拆成三部分：任务内容、负责人、计划完成时间；不能只写一句整句描述。
14. 拆分行动项时，如果原文里有“由我方负责”“由李处长协调”“刘主任负责”这类负责人前缀，要把它们移到“负责人”字段，不要保留在“任务内容”字段开头。
15. 负责人字段尽量简洁，优先写“我”“李处长”“刘主任”等，不要写成“我方：我”或“负责人：我方：我”。
16. 只能使用模板中已有的小标题和字段名，不要新增“希望的新平台”“项目大时间规划”这类新标题；相关内容要并入最接近的原字段。
17. 提及其他相关方时，保持原文实体完整自然，例如“校长办公会”，不要输出不自然的截断词，例如“上校长”。
18. 如果某字段下有子项，例如“业务模式与产品/服务”“当前使用工具/系统（如有）”，子项标题保留，子项内容写在标题下方，不要只输出子项标题。

当前时间：${currentDateTime}
我方姓名：${fullName}
我方职位：${position}

【大纲】
${template}

【语料】
${corpus}

现在直接输出填写完成后的结构化记录 markdown 正文。`;
}
